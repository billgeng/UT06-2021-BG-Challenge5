var timerCurrent = moment().format("dddd, MMMM Do ,YYYY");
var timer = document.querySelector('#timer');
timer.innerText = timerCurrent;

const schedulerText9Input = document.querySelector('#text-9');
const text9 = [];
const submitButton9 = document.querySelector('#submitBtn9');
const storedInput9 = localStorage.getItem('scheduleinput9');



schedulerText9Input.addEventListener('input',schedule => {
    text9.textContent = schedule.target.value;
    
})

const saveToLocalStorage9 = () => {
    localStorage.setItem('scheduleinput9', text9.textContent);
}

submitButton9.addEventListener('click',saveToLocalStorage9);
document.getElementById('#text-9').innerHTML = storedInput9; 

